z1    = np.array(z1_raw)
z2    = np.array(z2_raw)
freqs = np.arange(10_000, 100_000, 1_000)   # 10 kHz → 99 kHz, step 1 kHz
Rfb   = 119e3                                # 119 kΩ

# ── RC correction factor ───────────────────────────────────────────────────────
def correction_factor(C):
    return np.sqrt(1 + (2 * np.pi * freqs * Rfb * C) ** 2)

# ── Cost function: minimise normalised variance (CV²) of both datasets ─────────
def cost(log_C):
    C        = 10 ** log_C
    cf       = correction_factor(C)
    z1c, z2c = z1 / cf, z2 / cf
    cv1      = np.var(z1c) / np.mean(z1c) ** 2
    cv2      = np.var(z2c) / np.mean(z2c) ** 2
    return cv1 + cv2

# ── Iterative optimisation over log10(C) in [1e-13, 1e-8] ─────────────────────
result = minimize_scalar(cost, bounds=(-13, -8), method='bounded')
C_opt  = 10 ** result.x
print(f"Optimal C = {C_opt * 1e12:.3f} pF")

# ── Apply correction ───────────────────────────────────────────────────────────
cf_opt    = correction_factor(C_opt)
z1_corr   = z1 / cf_opt
z2_corr   = z2 / cf_opt
freqs_kHz = freqs / 1e3

print(f"Z1: raw std={z1.std():.1f} Ω  →  corrected std={z1_corr.std():.1f} Ω")
print(f"Z2: raw std={z2.std():.1f} Ω  →  corrected std={z2_corr.std():.1f} Ω")

